--RFC2 MOSTRAR LOS 20 PUNTOS DE VACUNACIÓN MÁS EFECTIVOS

SELECT IDPUNTOVACUNACION, COUNT(*)
FROM CITAS
WHERE FECHAYHORA
    BETWEEN TO_TIMESTAMP('2021-04-11 06:14:00.0', 'YYYY-MM-DD HH24:MI:SS.FF')
    AND TO_TIMESTAMP('2021-08-11 23:59:00.0', 'YYYY-MM-DD HH24:MI:SS.FF')
AND ESTADOCITA='ASISTIDA'
GROUP BY IDPUNTOVACUNACION
ORDER BY COUNT(*)
FETCH FIRST 20 ROWS ONLY;